fun main(){
    var a=40;
    var b:Float=a.toFloat()
    var c:Double=a.toDouble()
    var d:Byte=a.toByte();
    var e:Long=a.toLong();
    var f:Short=a.toShort();
    println("Coversion from int to float is $a to $b")
    println("Coversion from int to double is $a to $c")
    println("Coversion from int to byte is $a to $d")
    println("Coversion from int to long is $a to $e")
    println("Coversion from int to short is $a to $f")
}